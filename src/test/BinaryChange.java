package test;

/**
 * java中的进制表达方式
 */
public class BinaryChange {
    public static void main(String[] args) {
        int e = 0b10;//代表二进制数0010->2
        int f = 016;//代表八进制数16->14
        int g = 0x19;//代表十六进制数19->25
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }
}
